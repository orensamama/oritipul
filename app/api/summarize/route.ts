export async function POST(req: NextRequest) {
  const apiKey = process.env.OPENAI_API_KEY;

  try {
    const body = await req.json() as {
      text?: string;
      imageBase64?: string;
      mimeType?: string;
      style?: string;
    };

    // Simulation mode — return mock immediately (no API key)
    if (!apiKey) {
      await new Promise((r) => setTimeout(r, 1500));
      return NextResponse.json(MOCK_SUMMARY);
    }

    const styleNote = body.style === "short"
      ? "כתוב בצורה תמציתית וממוקדת."
      : body.style === "thematic"
      ? "הדגש חלוקה לפי תמות ונושאים."
      : "כתוב בצורה קלינית מורחבת ומפורטת.";

    const userContent: object[] = [];

    if (body.imageBase64) {
      userContent.push({
        type: "text",
        text: `קרא את הכתוב בתמונה זו (הערות מחברת טיפולית) וסכם לפי ההנחיות. ${styleNote}`,
      });
      userContent.push({
        type: "image_url",
        image_url: {
          url: `data:${body.mimeType ?? "image/jpeg"};base64,${body.imageBase64}`,
        },
      });
    } else {
      userContent.push({
        type: "text",
        text: `סכם את הפגישה הטיפולית הבאה. ${styleNote}\n\n═══ תמלול מלא של הפגישה ═══\n${body.text}\n═══ סוף התמלול ═══`,
      });
    }

    const res = await fetch("https://api.openai.com/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${apiKey}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "gpt-4o",
        messages: [
          { role: "system", content: SYSTEM_PROMPT },
          { role: "user",   content: userContent },
        ],
        response_format: { type: "json_object" },
        temperature: 0.2,
        max_tokens: 2000,
      }),
    });

    if (!res.ok) {
      const errText = await res.text();
      return NextResponse.json({ error: errText }, { status: res.status });
    }

    const data = await res.json();
    const parsed = JSON.parse(data.choices[0].message.content);
    return NextResponse.json(parsed);
  } catch (err) {
    return NextResponse.json({ error: String(err) }, { status: 500 });
  }
}
